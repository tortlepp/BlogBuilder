package eu.ortlepp.blogbuilder.model;

/**
 * An Enum for Document types. The type of the document / Markdown file is defined
 * in the header of the file. Currently, there are two types of documents defined.
 *
 * @author Thorsten Ortlepp
 */
public enum DocumentType {

    /** The type of the document is (blog) post. */
    POST,

    /** The type of the document is page. */
    PAGE;

}
